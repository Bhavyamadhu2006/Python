# Example for If Elif and Else statements
Job = "Software Engineer"

if (Job == "Teacher"):
  print("I'm working as a teacher")
elif (Job == "Doctor"):
  print("I'm a surgeon")
elif (Job == "Software Engineer"):
  print("I'm a developer")
else :
  print("I'm still studying")

