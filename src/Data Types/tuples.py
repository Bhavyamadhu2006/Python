t = (1,1,1,1,1,1,1,2,3,4,5)
l = [1,2,3,4,5]

# Get the type of t
type_of_t = type(t)
print(type_of_t)

# get the type of l
type_of_l = type(l)
print(type_of_l)

# Get the length of t
length_of_t = len(t)
print(length_of_t)

# Get the length of l
length_of_l = len(l)
print(length_of_l)

# Get the value of some element in t
get_t_value = t[2]
print(get_t_value)

# get the count of total elements in t
get_count = t.count(1)
print(get_count)